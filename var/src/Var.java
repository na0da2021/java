public class Var{
    public static void main(String[] args){
        /*
        عندنا اكتر من نوع للمتغيرات
        هنتعلم النهاردة (int, float, double)
         */
        // this is a project to learn variables

        // int --> للأرقام الصحيحة
        int age = 23;
        // float --> للأرقام العشرية بس عندي من 6 ل 7 ارقام بس بعد العلامة العشرية
        float gpa = 3.5998635f;
        // double --> double ال float --> فممكن اخزن فيها لحد من 15 ل 16 رقم بعد العلامة العشرية
        double x = 1.259874365124986;
        double y = 1.259874365124986222;

        System.out.println(age);
        System.out.println(gpa);
        System.out.println(x);
        System.out.println(y); //supposed to ignore the last 3 numbers
    }

}